import os

os.environ['OPENBLAS_NUM_THREADS'] = '1'

from sklearn.preprocessing import OrdinalEncoder
from sklearn.tree import DecisionTreeClassifier
from sklearn.metrics import accuracy_score, precision_score


def main(dataset):
    X = int(input())
    criteria = 'gini'
    classifier = DecisionTreeClassifier(criterion=criteria, random_state=0)
    encoder = OrdinalEncoder()
    encoder.fit([row[:-1] for row in dataset])

    threshold = len(dataset) - X
    train_set = dataset[:threshold]
    train_x = [row[:-1] for row in train_set]
    train_y = [row[-1] for row in train_set]
    train_x = encoder.transform(train_x)

    test_set = dataset[threshold:]
    test_x = [row[:-1] for row in test_set]
    test_y = [row[-1] for row in test_set]
    test_x = encoder.transform(test_x)

    classifier.fit(train_x, train_y)
    predictions = classifier.predict(test_x)
    accuracy = accuracy_score(test_y, predictions)
    precision = precision_score(test_y, predictions, pos_label="1", zero_division=0)
    print(f"Accuracy: {accuracy}")
    print(f"Precision: {precision}")


